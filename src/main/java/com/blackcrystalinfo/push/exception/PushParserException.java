package com.blackcrystalinfo.push.exception;

public class PushParserException extends PushException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5274833100183415982L;

	public PushParserException() {
		super();
	}

	public PushParserException(String msg) {
		super(msg);
	}

	public PushParserException(Throwable cause) {
		super(cause);
	}

	public PushParserException(String msg, Throwable cause) {
		super(msg, cause);
	}
}
